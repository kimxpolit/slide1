public class MayTinh {
    public int tinhTong(int a, int b) {
        return a + b;
    }
    
    public double tinhTong(double a, int b) {
        return a + b;
    }
    
    public int tinhTong(int a, int b, int c) {
        return a + b + c;
    }
}
